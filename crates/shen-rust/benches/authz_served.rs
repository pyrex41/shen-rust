//! Authz-shaped served benchmark: the >=1.5x kill-gate measurement for
//! the AOT-native loaded-Shen overlay (Lever B productionization).
//!
//! Workload = the shen-cedar served shape: load the committed authz spec
//! (`examples/shen-cedar-authz/spec/verify.shen` + `spec/authz.shen`, the
//! REAL verifier the example runs) plus a scaled deterministic data corpus
//! (`benches/authz_bench_data.shen`: 64-role DAG in depth-8 chains, 12
//! forbids x 48 permits, 30 grants), then serve many evaluations:
//!
//!   * verify-shape: `(classify-all (bench-forbids) (bench-permits)
//!     (bench-edges))` — 576 hierarchy-aware classify calls per query,
//!     each transitively resolving `in` over the role DAG (`reaches`).
//!   * generate-shape: `(expand-all)` — transitive grant closure over the
//!     scaled role DAG.
//!
//! The sweep runs in-language (one eval per query, recursion inside Shen)
//! — how a real served verifier batches — so the timed region measures
//! Shen execution, not marshalling. Cedar parse/validation is load-time
//! work in the served model and stays outside this bench entirely (the
//! example wiring stage cross-checks the generated-Cedar artifact).
//!
//! Method (normal_form_aot.rs verbatim): boot, load the spec files for
//! real on the session engine (tree-walk by default, VM with
//! `SHEN_RUST_VM=1`), record reference results, time min-of-N batches,
//! `gen::install()` the klcompile AOT overlay over the loaded defuns,
//! assert shen_eq identity on every query, re-time. Loaded-vs-AOT is
//! paired in-process; tree-vs-VM is cross-process (engine is
//! process-global) — drive interleaved pairs from a shell.
//!
//! After timing, a redefinition leg redefines `base-grants` over the
//! installed overlay and asserts `(expand-all)` reflects it through the
//! AOT dispatch path — the bench doubles as a live two-table coherence
//! check (see tests/aot_redefine_coherence.rs for the focused suite).
//!
//! The AOT module is generated by the canonical recipe — fresh boot,
//! `(bootstrap <file>)` per spec file, concatenate the .kl (bench data
//! last: KL last-wins redefines authz.shen's three data defuns at bench
//! scale), `klcompile --external`, rustfmt — and checked in under
//! `benches/gen/authz_served_gen.rs` (25 compiled / 0 skipped).
//!
//!   cargo bench --bench authz_served                  # tree-walk vs AOT
//!   SHEN_RUST_VM=1 cargo bench --bench authz_served   # VM vs AOT
//!
//! KILL-GATE: the AOT arm must be >= 1.5x over the VM-loaded arm
//! (min-of-10 ms/batch, paired in-process, same-session interleaved
//! runs) with identity green before any default-on consideration.

